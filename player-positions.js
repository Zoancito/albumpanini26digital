/** Devuelve la posición de un jugador o null si está pendiente */
export function getPlayerPosition(name) {
  return PLAYER_POSITIONS[name] ?? null
}

/** Devuelve todos los jugadores pendientes (null) */
export function getPendingPlayers() {
  return Object.entries(PLAYER_POSITIONS)
    .filter(([, pos]) => pos === null)
    .map(([name]) => name)
}
